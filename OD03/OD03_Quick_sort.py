# Функция для быстрой сортировки расходов по второму элементу кортежа (сумме)
def quick_sort(arr):
    if len(arr) <= 1:
        return arr

    pivot = arr[0][1]  # Опорный элемент — это сумма расходов (второй элемент кортежа)

    left = [i for i in arr if i[1] < pivot]  # Все элементы меньше опорного
    center = [i for i in arr if i[1] == pivot]  # Все элементы равны опорному
    right = [i for i in arr if i[1] > pivot]  # Все элементы больше опорного

    # Рекурсивно сортируем левую и правую часть
    return quick_sort(left) + center + quick_sort(right)


# Основная программа
if __name__ == "__main__":
    # Словарь с расходами за 12 месяцев
    expenses = {
        "Январь": 25000,
        "Февраль": 22000,
        "Март": 23000,
        "Апрель": 21000,
        "Май": 28000,
        "Июнь": 26000,
        "Июль": 27000,
        "Август": 24000,
        "Сентябрь": 25500,
        "Октябрь": 23000,
        "Ноябрь": 29000,
        "Декабрь": 31000
    }

    # Преобразуем словарь в список кортежей (месяц, расходы)
    expense_list = list(expenses.items())

    # Сортируем расходы по значениям (по возрастанию)
    sorted_expenses = quick_sort(expense_list)

    # Вывод исходных данных
    print("Исходные данные (месяц: расходы):")
    for month, cost in expenses.items():
        print(f"{month}: {cost} руб.")

    # Вывод отсортированных данных
    print("\nОтсортированные данные (по возрастанию расходов):")
    for month, cost in sorted_expenses:
        print(f"{month}: {cost} руб.")
